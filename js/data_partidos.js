var partidos_ganados =[];
var partidos_empatados =[];
var partidos_perdidos =[];
var partidos_menoresa=[];
var partidos_menoresb=[];
var partidos_menoresc=[];
var partidos_menoresd=[];
var partidos_copa=[];
var partidos_cancha1=[];
var partidos_cancha2=[];
var partidos_cancha3=[];

Ordenarx_resultado(partidos);
Ordenarx_categoria(partidos);
Ordenarx_cancha(partidos);



function Ordenarx_resultado(games){
   games.forEach(function(partido){
    if (partido.goles_local > partido.goles_rival){
            partidos_ganados.push(partido);
    } else if (partido.goles_local == partido.goles_rival){
            partidos_empatados.push(partido);
    } else {
        partidos_perdidos.push(partido);
    }

   })
};


function Ordenarx_categoria(games){
   games.forEach(function(partido){
           switch (partido.categoria){
                   case "Menores D":
                           partidos_menoresd.push(partido);
                           break;
                   case "Menores C":        
                           partidos_menoresc.push(partido);
                           break; 
                   case "Menores B":        
                           partidos_menoresb.push(partido);
                           break;   
                   case "Menores A":        
                           partidos_menoresa.push(partido);
                           break;    
                   default:        
                           partidos_copa.push(partido);
                           break; 
           }            
   })   
}

function Ordenarx_cancha(games){
        games.forEach(function(partido){
           switch (partido.cancha){
              case 1:
               partidos_cancha1.push(partido);
               break;
              case 2:
                partidos_cancha2.push(partido);
                break;
              case 3:
                partidos_cancha3.push(partido);
                break;               
    
            }   
        })
}


function Sumar_goles(nombre,categoria){
        var scores=0;
        categoria.forEach(partido => {
            partido.goles.forEach(player => {
                    if (player == nombre){
                        scores++
                    }
                })
            })
        return scores;
};